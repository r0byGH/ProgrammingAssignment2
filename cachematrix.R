cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'

  s <- x$getsolve()
  if(!is.null(s)) {
    message("getting cached data")
    return(s)
  }
  data <- x$get()
  ##i <- identity matrix with same dimension as x 
  i <- diag(nrow(data))
  s <- solve(data, i, ...)
  x$setsolve(s)
  s
}
